export type Category = {
  id: string;
  createdAt: string;
  updatedAt: string;
  publishedAt: string;
  revisedAt: string;
  name: string;
};

export type Blog = {
  id: string;
  createdAt: string;
  updatedAt: string;
  publishedAt: string;
  revisedAt: string;
  title: string;
  description: string;
  content: string;
  categories: Array<Category>;
};

export type ContentList<T> = {
  contents: Array<T>;
  totalCount: number;
  offset: number;
  limit: number;
};
